package org.example;

public class MainClass9WhileLoop
{
    public static void main(String[] args)
    {
     int i = 10;
     while(i>1)
     {
         System.out.println(i);
         i--;
     }

     //Do While Loop
     int k = 10;
     do
     {
         System.out.println(k);
         k--;
     }
     while(k>1);
    }
}
