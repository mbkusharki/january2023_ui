package org.example;

public class MainClass8ForLoop
{
    public static void main(String[] args)
    {
        //increment loop
        for (int i = 0; i < 10; i++)
        {
            System.out.println(i);
        }
        //decrement loop
        //printout number in descending order from 50 to 30
        for (int j = 50; j >= 30; j--)
        {
         System.out.println(j);
        }
    }
}
